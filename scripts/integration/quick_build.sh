        "$PROJECT_ROOT/src/Module/Yuigahama/CMOD/MusicPlayer" >/dev/null 2>&1
    
    local package_count=$(find "$DIST_DIR/packages" -name "*.cmod" | wc -l)
    print_success "Packaged $package_count CMOD modules"
}

